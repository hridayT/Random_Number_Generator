"""
The aim here is to carry out a computation that otherwise is intractable on the
machine: (1000000!)/(100000!)^10
To do this:
    *prepare training set by computing (n/10 in n) s.t. n is divisible by 10
    * Hence compute n!/(n/10!)^10 for the max amount of values
    *Linear/randomforest regression on these values
    * Compute for 1000000
"""


def fact(n):
    fac = 1
    for i in range(n):
        fac = fac * (n - i)
    return fac

# n must be divisible by 10


def compute(n):
    return (fact(n) // pow(fact(n // 10), 10)) / pow(10, n)


